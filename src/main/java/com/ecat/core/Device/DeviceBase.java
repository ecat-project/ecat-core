    private Map<String, AttributeBase<?>> attrs;

    // 原子类保证多线程安全的自增ID生成器
    private static final AtomicLong OBJECT_ID_GENERATOR = new AtomicLong(1);
    // 每个设备对象的全局唯一ID
    @Getter
    private final long objectId;

    protected final I18nProxy i18n = I18nHelper.createProxy(this.getClass());


    /**
     * 从 ConfigEntry 构建设备（推荐）。
     *
     * <p>保存 entry 引用建立设备与配置的一对一关系。此时 {@link #getId()} 返回
     * {@code entry.getEntryId()}（系统生成 UUID，设备注册表以此 entryId 为 key），
     * {@link #getUniqueId()} 返回 {@code entry.getUniqueId()}（业务标识，如 厂家_sn）。
     *
     * @param entry 配置条目
     */
    public DeviceBase(ConfigEntry entry) {
        this.log = LogFactory.getLogger(getClass());
        this.objectId = OBJECT_ID_GENERATOR.getAndIncrement();
        this.entry = entry;
        this.config = entry.getData();
        this.id = java.util.UUID.randomUUID().toString();
        this.uniqueId = entry.getUniqueId();
        initDevice(entry.getData());
    }

    /**
     * 网关子设备构造：无自身 entry，复用网关 entry 作 1:N back-ref。
     * <p>uniqueId 由集成从子设备硬件读取（≠ 网关 entry 的 uniqueId）；id 仍由 core 铸造 UUID，
     * 经 DeviceRegistry.getOrCreate 用持久化值覆盖以跨重启稳定。
     *
     * @param gatewayEntry 创建此子设备的网关 ConfigEntry（getEntry().getEntryId() 即网关 entryId）
     * @param uniqueId     子设备硬件原生 uniqueId
     * @param config       子设备运行时配置（由网关组装）
     */
    public DeviceBase(ConfigEntry gatewayEntry, String uniqueId, Map<String, Object> config) {
        this.log = LogFactory.getLogger(getClass());
        this.objectId = OBJECT_ID_GENERATOR.getAndIncrement();
        this.entry = gatewayEntry;
        this.config = config;
        this.id = java.util.UUID.randomUUID().toString();
        this.uniqueId = uniqueId;
        initDevice(config);
    }

    /**
     * 从 config Map 构建设备（旧式，已废弃）。
     * <p>新代码用 {@link #DeviceBase(ConfigEntry)}（entry-backed）或
     * {@link #DeviceBase(ConfigEntry, String, Map)}（网关子设备）。00-core Task 11 删除此构造。
     *
     * @param config 设备配置
     * @deprecated 使用 {@link #DeviceBase(ConfigEntry)} 或网关构造器替代
     */
    @Deprecated
    public DeviceBase(Map<String, Object> config) {
        this.log = LogFactory.getLogger(getClass());
        this.objectId = OBJECT_ID_GENERATOR.getAndIncrement();
        this.config = config;
        this.entry = new ConfigEntry(); // create empty config entry temporary
        this.id = java.util.UUID.randomUUID().toString();
        this.uniqueId = null;
        initDevice(config);
    }

    @SuppressWarnings("unchecked")
    private void initDevice(Map<String, Object> config) {
        this.name = (String) config.get("name");

        // sn is optional , if config not has sn set sn = null
        this.deviceClass = DeviceClasses.getEnum((String) config.getOrDefault("class", null));
        this.deviceStatus = DeviceStatus.UNKNOWN;
        this.abilities = new ArrayList<>();
        List<String> abilities = (List<String> ) config.getOrDefault("abilities", null);
        if(abilities != null){
            for (String ability : abilities) {
                this.abilities.add(DeviceAbility.getEnum(ability));
            }
        }

        this.sn = (String) config.getOrDefault("sn", null);
        this.vendor = (String) config.getOrDefault("vendor", null);
        this.model = (String) config.getOrDefault("model", null);

        this.attrs = new HashMap<>();
    }

